// Qual é a função do código de status 400 e 422?

// Status 400:
//  // O código de status de resposta HTTP 400 Bad Request
//  // indica que o servidor não pode ou não irá processar a requisição devido
//  // a alguma coisa que foi entendida como um erro do cliente (por exemplo,
//  // sintaxe de requisição mal formada, enquadramento de mensagem de
//  // requisição inválida ou requisição de roteamento enganosa).

// Status 422
//  // O codigo de resposta HTTP 422 Unprocessable Entity indica que
//  // o servidor entende o tipo de conteúdo da entidade da requisição, e
//  // a sintaxe da requisição esta correta, mas não foi possível
//  // processar as instruções presentes.

